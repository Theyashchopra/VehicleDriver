package com.lifecapable.vehicledriver.Driver.datamodels;

public class HomeDriverData {
    String name,time, address, contact;
    float lat, lon;

    public HomeDriverData(String name, String time, String address, String contact, float lat, float lon) {
        this.name = name;
        this.time = time;
        this.address = address;
        this.contact = contact;
        this.lat = lat;
        this.lon = lon;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public float getLat() {
        return lat;
    }

    public void setLat(float lat) {
        this.lat = lat;
    }

    public float getLon() {
        return lon;
    }

    public void setLon(float lon) {
        this.lon = lon;
    }
}
